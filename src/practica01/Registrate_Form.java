    private void jButton_RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegistrarActionPerformed

        String cNombre = jTextField_Nombre.getText();
        String cNombreU = jTextField_NombreUsuario.getText();
        String cApellido = jTextField_Apellido.getText();
        String cTelefono = jTextField_NumTelefono.getText();
        String cCorreoE = jTextField_CorreoElec.getText();
        String clave1 = String.valueOf(jPasswordField_1.getPassword());
        String clave2 = String.valueOf(jPasswordField_2.getPassword());
        
        if(verificarCampos()){
            
            if(!verificarUsuario(cNombreU)){
              
            PreparedStatement ps;
            ResultSet rs;
            String registrarUsuarioQuery = "INSERT INTO `usuarios`(`Nombre`, `usuario`, `Clave`, `Apellido`, `Telefono`, `Correo`) VALUES (?, ?, ?, ?, ?, ?)";
            
                try {
                    ps = Mi_CNX.getConnection().prepareStatement(registrarUsuarioQuery);
                    ps.setString(1, cNombre);
                    ps.setString(2, cNombreU);
                    ps.setString(3, clave1);
                    ps.setString(4, cApellido);
                    ps.setString(5, cTelefono);
                    ps.setString(6, cCorreoE);
                    
                    if(ps.executeUpdate() !=0){
                        JOptionPane.showMessageDialog(null, "Tu cuenta ha sido creada.");
                    } else {
                        JOptionPane.showMessageDialog(null, "Error: revisa tu información.");
                    }
                                           
                } catch (SQLException ex) {
                    Logger.getLogger(Registrate_Form.class.getName()).log(Level.SEVERE, null, ex);
                }           
            }
        }
       
    }//GEN-LAST:event_jButton_RegistrarActionPerformed

