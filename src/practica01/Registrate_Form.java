    //Crear una función para verificar los campos vacíos
    
    public boolean verificarCampos(){
    
        String cNombreU = jTextField_NombreUsuario.getText();
        String cNombre = jTextField_Nombre.getText();
        String cApellido = jTextField_Apellido.getText();
        String cTelefono = jTextField_NumTelefono.getText();
        String cCorreoE = jTextField_CorreoElec.getText();
        String clave1 = String.valueOf(jPasswordField_1.getPassword());
        String clave2 = String.valueOf(jPasswordField_2.getPassword());
         
        //Verificar campos vacíos
        if(cNombre.trim().equals("")){
        
            JOptionPane.showMessageDialog(null, "Debes ingresar tu nombre", "Nombre vacío", 2);
        }
        else if(cNombreU.trim().equals("")){
            
            JOptionPane.showMessageDialog(null, "Debes ingresar un nombre de usuario", "Usuario vacío", 2);
        }
        else if(cApellido.trim().equals("")){
            
            JOptionPane.showMessageDialog(null, "Debes ingresar su apellido", "Apellido vacío", 2);
        }
        else if(cTelefono.trim().equals("")){
            
            JOptionPane.showMessageDialog(null, "Debes ingresar su teléfono", "Número de teléfono vacío", 2);
        }
        else if(cCorreoE.trim().equals("")){
            
            JOptionPane.showMessageDialog(null, "Debes ingresar un correo electrónico", "Correo vacío", 2);
        }
        else if(clave1.trim().equals("")){
            
            JOptionPane.showMessageDialog(null, "Debes ingresar una contraseña", "Contraseña vacía", 2);
        }
        else if(clave2.trim().equals("")){
            
            JOptionPane.showMessageDialog(null, "Debes confirmar la contraseña", "Confirmación vacía", 2);
        }
        
        //Verificar si las dos contraseñas coinciden  
        if (!clave1.equals(clave2))
        {
            JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden", "Confirma la contraseña", 2);
            return false;
        }
        
        //Si todo está correcto      
        else 
        {
            return true;
        }   
    }
    
    //Cree una función para verificar si el nombre de usuario ingresado ya existe en la base de datos
    
    public boolean verificarUsuario(String cNombreU){
    
        PreparedStatement st;
        ResultSet rs;
        boolean usuario_existe = false;
        
        String query = "SELECT * FROM `usuarios` WHERE `usuario` = ?";
        
        try {
            
            st = Mi_CNX.getConnection().prepareStatement(query);
            st.setString(1, cNombreU);
            rs = st.executeQuery();
            
            if(rs.next()){
                usuario_existe = true;
                JOptionPane.showMessageDialog(null, "Este usuario ya existe. Elige otro.", "El nombre de usuario falló", 2);
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Registrate_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return usuario_existe;
    }
    
    
